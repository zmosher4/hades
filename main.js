const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout,
});

let chosenGod = '';

// Ask the user for input
rl.question(
  'Type "random" to generate a random build or "custom" to choose a custom god: ',
  (input) => {
    if (input.toLowerCase() === 'random') {
      console.log('Here is your build.');
      const attack = attackGenorator(database);
      console.log(
        `Your attack is ${attack.title} from ${attack.god}. ${attack.description}`
      );
      const special = specialGenorator(database);
      console.log(
        `Your special is ${special.title} from ${special.god}. ${special.description}`
      );
      const cast = castGenorator(database);
      console.log(
        `Your cast is ${cast.title} from ${cast.god}. ${cast.description}`
      );
      const dash = dashGenorator(database);
      console.log(
        `Your dash is ${dash.title} from ${dash.god}. ${dash.description}`
      );
      const call = callGenorator(database);
      console.log(
        `Your call is ${call.title} from ${call.god}. ${call.description}`
      );
    } else if (input.toLowerCase() === 'custom') {
      rl.question(
        `choose from these gods (1-7)
            
            1. Ares
            2. Aphrodite
            3. Artemis
            4. Poseiden
            5. Zeus
            6. Athena
            7. Demeter
                
        `,
        (input) => {
          if (input === '1') {
            chosenGod = 'Ares';
          } else if (input === '2') {
            chosenGod = 'Aphrodite';
          } else if (input === '3') {
            chosenGod = 'Artemis';
          } else if (input === '4') {
            chosenGod = 'Poseidon';
          } else if (input === '5') {
            chosenGod: 'Zeus';
          } else if (input === '6') {
            chosenGod = 'Athena';
          } else if (input === '7') {
            chosenGod = 'Demeter';
          } else {
            console.log('not a god');
          }
          console.log(
            `The God you have chosen for this run is ${chosenGod}. 
            Your attack is - ${showAttack(chosenGod, database)}
            Your special is - ${showSpecial(chosenGod, database)}
            Your cast is - ${showCast(chosenGod, database)}
            Your dash is - ${showDash(chosenGod, database)}
            Your call is - ${showCall(chosenGod, database)}
            `
          );
        }
      );
    }
  }
);
